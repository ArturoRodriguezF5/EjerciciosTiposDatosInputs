package ejercicios.tiposdedatosavanzados;

public class ArrayUnidimensional01 {
    public static void main(String[] args) {

        // Creamos un Array de tipo String
        String[] frutas = {"Melón", "Sandía", "Plátano", "Tuna", "Kiwi"};

        // Recorremos el Array para mostrar sus valores.
        for (String fruta : frutas) {
            System.out.println(fruta);
        }
    }
}
